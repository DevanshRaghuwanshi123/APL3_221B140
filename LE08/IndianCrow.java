class IndianCrow implements Crow {
    @Override
    public void cry() {
        System.out.println("IndianCrow is cawing!");
    }

    @Override
    public void fly() {
        System.out.println("IndianCrow is flying!");
    }

    @Override
    public void eat() {
        System.out.println("IndianCrow is eating!");
    }
}
